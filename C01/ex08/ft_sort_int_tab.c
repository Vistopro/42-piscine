void	ft_sort_int_tab(int *tab, int size)
{
	int	a;
	int	i;
	int	j;

	i = 0;
	while (i < size - 1)
	{
		j = i + 1;
		while (j < size)
		{
			if (tab[i] > tab[j])
			{
				a = tab[i];
				tab[i] = tab[j];
				tab[j] = a;
			}
		j++;
		}
		i++;
	}	
}
